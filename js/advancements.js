function generateAdvancementBadge(name) {
	if(!(name in advancements)) {
		return document.createTextNode(name)
	}
	let advancement = advancements[name]

	let badge = document.createElement("a")
	badge.classList.add("advancement")
	if(name.substr(-5) === "/root") {
		badge.classList.add("root")
	}
	if("wiki" in advancement) {
		badge.href = `https://minecraft.wiki/w/Tutorials/Advancement_guide/${advancement.wiki}`
		badge.target = "_blank"
	}

	let content = document.createElement("div")
	content.classList.add("vcenter")

	let icon = document.createElement("img")
	icon.classList.add("icon")
	icon.src = `https://minecraft.wiki/images/${advancement.icon}`

	content.appendChild(icon)
	content.appendChild(document.createTextNode(advancement.title))
	badge.appendChild(content)
	return badge
}
